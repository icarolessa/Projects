// Calculando área do quadrado

const baseQuadrado = 4
const alturaQuadrado = 3
const areaQuadrado = (baseQuadrado * alturaQuadrado).toFixed(2)

console.log(`A área do quadrado de base ${baseQuadrado}m e altura ${alturaQuadrado}m é ` + areaQuadrado + 'm². E o dobro da área é: ' + (areaQuadrado * 2) + 'm²')